public class InstructionSimple extends Instruction {
    public InstructionSimple(char motAlire) {
        this.motAlire = motAlire;
    }

    public void setMotAlire(char motAlire) {
        this.motAlire = motAlire;
    }

    public char getMotAlire() {
        return motAlire;
    }

    private char motAlire;
}
